public class Point {

	private String position;
	
	private String sample_area;
	
	private String sample_city;
	
	private String sample_lat;
	
	private String sample_lon;
	
	private String sample_provice;
	
	public String getPosition() {
		return position;
	}
	
	public void setPosition(String position) {
		this.position = position;
	}
	
	public String getSample_area() {
		return sample_area;
	}
	
	public void setSample_area(String sample_area) {
		this.sample_area = sample_area;
	}
	
	public String getSample_city() {
		return sample_city;
	}
	
	public void setSample_city(String sample_city) {
		this.sample_city = sample_city;
	}
	
	public String getSample_lat() {
		return sample_lat;
	}
	
	public void setSample_lat(String sample_lat) {
		this.sample_lat = sample_lat;
	}
	
	public String getSample_lon() {
		return sample_lon;
	}
	
	public void setSample_lon(String sample_lon) {
		this.sample_lon = sample_lon;
	}
	
	public String getSample_provice() {
		return sample_provice;
	}
	
	public void setSample_provice(String sample_provice) {
		this.sample_provice = sample_provice;
	}

}
